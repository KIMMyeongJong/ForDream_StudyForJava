public class MakeStar {

	public static void main(String[] args) {

		for (int i = 0; i <= 4; i++) {
			for (int k = 0; k <= 10; k++) {

				System.out.print("*");
				if (k == 10) {
					System.out.println();
				}//if end
			}//in for end
		}//out for end
	}

}
